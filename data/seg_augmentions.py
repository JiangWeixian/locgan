class SSDAugmentation(object):
    def __init__(self, size=300):
        self.size = size
        self.augment = Compose([
            RandomSampleCrop(),
            RandomMirror(),
            Resize(self.size),
        ])

    def __call__(self, img, mask):
        return self.augment(img, mask, mask)